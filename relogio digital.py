import time

while True:
    current_time = time.strftime("%H:%M:%S")
    print(current_time)
    time.sleep(1)
    
    ## Este código usa a biblioteca "time" para obter a hora atual e imprimi-la na tela no formato "HH:MM:SS". A função "sleep(1)" faz com que o código pare por um segundo antes de imprimir a hora novamente, criando assim a sensação de um relógio digital atualizando a cada segundo.

